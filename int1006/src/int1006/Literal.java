package int1006;

public class Literal {

	public static void main(String[] args) {
		String 이름 = "박 기쁨";
		int 나이 = 123;
		double 키 = 123;
		double 몸무게 = 123;
		char 성별 = 'F';
		boolean 결혼여부 = false;

		System.out.println("나이는 " + 나이);
		System.out.println("키는 " + 키);
		System.out.println("결혼여부 " + 결혼여부);
		
		
		System.out.println("이름 \t"+ 이름 + '\n' + "나이 \t" + 나이 + '\n' + "키 \t" + 키 + "\n" + "몸무게\t" + 몸무게 + '\n' + "성별\t" + 성별 + '\n');
		
		
		
	}

}
